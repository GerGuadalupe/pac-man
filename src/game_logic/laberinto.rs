pub fn maze_maker() -> Vec<Vec<Casilla>> {
    let mut lab = Vec::new();

    for fila in MAPA_LABERINTO {
        let mut f = Vec::new();
        for casilla in fila {
            match casilla {
                0 => f.push(Casilla::Vacio),
                1 => f.push(Casilla::Pared),
                2 => f.push(Casilla::Nodo(Arc::new(RwLock::new(Nodo::default())))),
                3 => f.push(Casilla::Spawn),
                _ => (),
            }
        }
        lab.push(f);
    }
    maze_init(&mut lab);
    lab
}
